package com.corejava;

public class SwitchCase {

    public static void main(String[] args) {
        int i =10;

        switch(i) {
            case 1:System.out.println("i is 5");
                break;
            case 5:System.out.println("i is 5");
                break;
            case 10:System.out.println("i is 10");
                break;
            default: System.out.println("case default");
        }
    }

}
